package week7b;

public class myJava701 {
	public static void main(String[]args) {
		char text1='M';
		char text2 = 'A';
		char aStart = '*';
		char text3='B';
		char text4='D';
		//escape sequence
		System.out.println("Hello\nther");
		System.out.println("Text1\b" +text1);
		System.out.println("Text2\t"+text2);
		System.out.println("aStart\r"+aStart);
		System.out.println("Text3\\" + text3);
		System.out.println("Text4\''"+text4);
		
	}
}
